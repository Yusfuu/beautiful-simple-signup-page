<script>
    function hide() {
        $('.overlay_content').css('opacity', '0');
        $('.overlay').css('width', '0%');
        $('.menu').css('opacity', '1');
    }

    $('.menu').click(() => {
        $('.menu').css('opacity', '0');
        $('.overlay').css('width', '100%');
        $('.overlay_content').css('opacity', '1');
    });

    $('.close').click(() => {
        hide();
    });
    $('body').keydown(e => {
        if (e.keyCode == 27) {
            hide();
        }
    });

    $('#to-top').click(function() {
        $('html').animate({
            scrollTop: 0
        }, "slow");
    });

    window.addEventListener('scroll', function(e) {
        let top = document.querySelector('#to-top');
        if (window.scrollY > 2500) {
            top.style.display = 'initial';
        } else {
            top.style.display = 'none';
        }
    })
</script>

